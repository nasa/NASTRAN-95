     *  .0050, -.0324, -.0324,  .0050, -.0324,  .2078,  .2078, -.0324,
     * -.0324,  .2078,  .2078, -.0324,  .0050, -.0324, -.0324,  .0050,
     * -.0008,  .0050,  .0050, -.0008,  .0050, -.0324, -.0324,  .0050,
     *  .0050, -.0324, -.0324,  .0050, -.0008,  .0050,  .0050, -.0008/
