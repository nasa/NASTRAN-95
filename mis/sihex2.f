      IF (ISOPL.NE.EQEXIN  .AND. OLDEID.NE.0) GO TO 10
      ISOPL=-1
      ITRL(1)=EQEXIN
      CALL RDTRL (ITRL)
      LEQX =ITRL(2)*2
      MXLEQ=(NEQU/2)*2
      NEQX =(LEQX-1)/MXLEQ+1
      L=LEQX
