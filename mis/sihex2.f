      IEND = 0
      CALL READ (*140,*140,EQEXIN,EQU,L,0,I)
      DO 5 I=2,L,2
    5 EQU(I)=IABS(EQU(I))
      CALL SORT (0,0,2,2,EQU,L)
      L=L/2
C
C     CONVERT SIL NUMBERS TO EXTERNAL GRID POINT NUMBERS
C
   10 OLDEID = IESTA(1)
      DO 15 I = 1,NGP
   15 EXTRNL(I)=-IESTA(I+1)
      DO 130 J=1,NEQX
   16 DO 120 I=1,NGP
      IF (EXTRNL(I) .GT. 0) GO TO 120
      SIL=IESTA(I+1)*10+1
