     * -.2152,  .1147, -.0565,  .0161, 1.3808, -.7358,  .3624, -.1030,
     * 1.3808, -.7358,  .3624, -.1030, -.2152,  .1147, -.0565,  .0161,
     * -.0008,  .0050,  .0050, -.0008,  .0050, -.0324, -.0324,  .0050,
