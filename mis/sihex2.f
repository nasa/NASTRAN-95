C*****
C     IF THIS IS THE FIRST STRESS POINT IN AN ELEMENT, INITIALIZE
C     COUNTER AND GET EXTERNAL GRID POINT NUMBERS
C*****
      IF (ISTORE .EQ. 1) GO TO 2633
      IF (OLDEID .EQ. IESTA(1)) GO TO 200
      STRSPT=0
      IPTS=(TYPE-1)/2
      NGP =12*TYPE-4
      NGP1=NGP+1
      IF (TYPE .EQ. 3) NGP1=21
      NIP=IESTA(20*NGP+9)
